// Helper function to get all services as a flat array
export const getAllServices = (): Service[] => {
  return serviceCategories.flatMap(category => category.services);
};

// Helper function to get a service by slug
export const getServiceBySlug = (slug: string): Service | undefined => {
  return getAllServices().find(service => service.slug === slug);
};

// Helper function to get services by category
export const getServicesByCategory = (categorySlug: string): Service[] => {
  const category = serviceCategories.find(cat => cat.slug === categorySlug);
  return category?.services || [];
};

// Helper function to get category by slug
export const getCategoryBySlug = (slug: string): ServiceCategory | undefined => {
  return serviceCategories.find(cat => cat.slug === slug);
};
